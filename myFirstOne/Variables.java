package myFirstOne;

public class Variables {
	
	

	public static void main(String[] args) {
		int x=100;
		switch(x){
		
		case 1:
			System.out.println("it is equal to one");
			break;
			
		case 100:
			System.out.println("it is equal to two");
			break;
			
		case 3:
			System.out.println("it is equal to three");
			break;
			
		case 4:
			System.out.println("it is equal to four");
			break;
			
		default:
			System.out.println("Just print nothing");
			
			
			
			
			
		}	
		
		
		
	}
		



}
